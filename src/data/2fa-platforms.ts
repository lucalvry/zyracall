export type ReliabilityLevel = 'high' | 'medium' | 'low' | 'blocked';
export type PlatformCategory = 'financial' | 'messaging' | 'social' | 'crypto' | 'other';
export type VerdictLevel = 'recommended' | 'caution' | 'avoid';
export type DataConfidence = 'high' | 'moderate' | 'limited';

export interface CountryCompatibility {
  country: string;
  countryCode: string;
  flag: string;
  mobile: ReliabilityLevel;
  voip: ReliabilityLevel;
  virtual: ReliabilityLevel;
  warnings?: string[];
}

export interface PrimaryRecommendation {
  country: string;
  countryCode: string;
  flag: string;
  numberType: 'mobile' | 'voip' | 'virtual';
  reason: string;
}

export interface RecoveryGuidance {
  alternativeType?: string;
  alternativeCountry?: string;
  retryLimit?: string;
  lockoutWarning?: string;
}

export interface Platform {
  id: string;
  name: string;
  icon: string;
  category: PlatformCategory;
  description: string;
  generalNotes: string[];
  lastUpdated: string;
  compatibility: CountryCompatibility[];
  // Decision guidance fields (optional - will be derived from platformDecisionData or generated)
  verdicts?: {
    mobile: VerdictLevel;
    voip: VerdictLevel;
    virtual: VerdictLevel;
  };
  verdictStatements?: {
    mobile: string;
    voip: string;
    virtual: string;
  };
  primaryRecommendation?: PrimaryRecommendation;
  recoveryGuidance?: RecoveryGuidance;
  dataConfidence?: DataConfidence;
}

