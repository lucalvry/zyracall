// Helper type for enriched platform with all decision fields
export type EnrichedPlatform = Platform & Required<Pick<Platform, 'verdicts' | 'verdictStatements' | 'primaryRecommendation' | 'recoveryGuidance' | 'dataConfidence'>>;

// Enrich a platform with decision data
const enrichPlatform = (platform: Platform): EnrichedPlatform => {
  const decisionData = getDecisionData(platform.id, platform.compatibility);
  return {
    ...platform,
    verdicts: platform.verdicts ?? decisionData.verdicts,
    verdictStatements: platform.verdictStatements ?? decisionData.verdictStatements,
    primaryRecommendation: platform.primaryRecommendation ?? decisionData.primaryRecommendation,
    recoveryGuidance: platform.recoveryGuidance ?? decisionData.recoveryGuidance,
    dataConfidence: platform.dataConfidence ?? decisionData.dataConfidence,
  };
};

// Helper functions
export const getPlatformById = (id: string): EnrichedPlatform | undefined => {
  const platform = platforms.find(p => p.id === id);
  return platform ? enrichPlatform(platform) : undefined;
};

export const getPlatformsByCategory = (category: PlatformCategory): EnrichedPlatform[] =>
  platforms.filter(p => p.category === category).map(enrichPlatform);

export const searchPlatforms = (query: string): EnrichedPlatform[] =>
  platforms.filter(p => 
    p.name.toLowerCase().includes(query.toLowerCase()) ||
    p.description.toLowerCase().includes(query.toLowerCase())
  ).map(enrichPlatform);

export const getCountryCompatibility = (
  platformId: string, 
  countryCode: string
): CountryCompatibility | undefined => {
  const platform = getPlatformById(platformId);
  return platform?.compatibility.find(c => c.countryCode === countryCode);
};

export const getAllCountries = (): { country: string; countryCode: string; flag: string }[] => {
  const seen = new Set<string>();
  const countries: { country: string; countryCode: string; flag: string }[] = [];
  
  for (const platform of platforms) {
    for (const compat of platform.compatibility) {
      if (!seen.has(compat.countryCode)) {
        seen.add(compat.countryCode);
        countries.push({
          country: compat.country,
          countryCode: compat.countryCode,
          flag: compat.flag,
        });
      }
    }
  }
  
  return countries.sort((a, b) => a.country.localeCompare(b.country));
};
